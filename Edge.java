import java.awt.*;

public class Edge {
    
    public int x1;
    public int x2;
    public int y1;
    public int y2;
    public int width;

    public Edge(int x1, int x2, int y1, int y2, int width) {
        x1 = x1;
        x2 = x2;
        y1 = y1;
        y2 = y2;
        width = width;
    }

}
